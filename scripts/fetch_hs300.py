def get_market_code(symbol):
    if symbol.startswith("sh"):
        return 1, symbol[2:]
    elif symbol.startswith("sz"):
        return 0, symbol[2:]
    return None, None

def fetch_batch_klines(symbols, retry=2):
    if not symbols:
        return {}
    codes = ",".join(symbols)
    url = f"https://quotes.sina.cn/cn/api/json_v2.php/CN_MarketData.getKLineData?symbol={codes}&scale=240&datalen=60&ma=no"
    for attempt in range(retry):
        try:
            req = urllib.request.Request(url, headers={
                "User-Agent": "Mozilla/5.0",
                "Referer": "https://finance.sina.com.cn"
            })
            import json as json2
            with urllib.request.urlopen(req, timeout=15) as resp:
                content = resp.read().decode("utf-8")
            data = json2.loads(content)
            result = {}
            if isinstance(data, list):
                for stock in data:
                    sym = stock.get("symbol", "")
                    d = stock.get("day", "")
                    o = float(stock.get("open", 0))
                    c = float(stock.get("close", 0))
                    h = float(stock.get("high", 0))
                    l = float(stock.get("low", 0))
                    v = float(stock.get("volume", 0))
                    if d and c > 0:
                        if sym not in result:
                            result[sym] = []
                        result[sym].append({"date": d, "open": o, "close": c, "high": h, "low": l, "volume": v})
            elif isinstance(data, dict):
                for sym, stock_data in data.items():
                    if isinstance(stock_data, list):
                        parsed = []
                        for d in stock_data:
                            if isinstance(d, dict) and "day" in d:
                                parsed.append({
                                    "date": d.get("day", ""),
                                    "open": float(d.get("open", 0)),
                                    "close": float(d.get("close", 0)),
                                    "high": float(d.get("high", 0)),
                                    "low": float(d.get("low", 0)),
                                    "volume": float(d.get("volume", 0))
                                })
                        if parsed:
                            result[sym] = parsed
            return result
        except Exception as e:
            if attempt < retry - 1:
                time.sleep(1)
            else:
                print(f"  [!] 批次失败: {e}", file=sys.stderr)
                return {}
    return {}

